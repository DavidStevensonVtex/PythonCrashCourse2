# Sorting a List Permanently with the sort() Method
cars = [ 'bmw', 'audi', 'toyota', 'subaru' ]
cars.sort()
print(cars)
# ['audi', 'bmw', 'subaru', 'toyota']

# Sorting a List Permanently in Reverse Order
cars = [ 'bmw', 'audi', 'toyota', 'subaru' ]
cars.sort(reverse=True)
print(cars)
# ['toyota', 'subaru', 'bmw', 'audi']