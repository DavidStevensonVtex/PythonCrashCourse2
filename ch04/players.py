players = [ 'charles', 'martina', 'michael', 'florence', 'eli' ]
print(players[0:3])
# ['charles', 'martina', 'michael']

print(players[1:4])
# ['martina', 'michael', 'florence']

print(players[:4])
# ['charles', 'martina', 'michael', 'florence']

print(players[2:])
# ['michael', 'florence', 'eli']

# This prints the last three players.
print(players[-3:])
# ['michael', 'florence', 'eli']